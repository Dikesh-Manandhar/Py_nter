"""
bitmap_utils.py — Shared 1-bit bitmap utilities for retro-style icons.

A bitmap is a list[str] where each string is one row of pixels.
    '#' = foreground (opaque pixel)
    '.' = background (transparent pixel)

COMPUTER GRAPHICS CONCEPT:
    1-bit bitmaps are the simplest raster format: each pixel is either
    ON or OFF. Early computer UIs (Mac System 1, Windows 1.0, GEM)
    used this technique for all icons and cursors.
"""

import pygame

# Type alias for clarity
Bitmap = list[str]


def bitmap_to_surface(
    bitmap: Bitmap,
    fg: tuple[int, int, int],
    scale: int = 1,
) -> pygame.Surface:
    """Render a 1-bit bitmap to a transparent pygame.Surface."""
    rows = len(bitmap)
    cols = max(len(r) for r in bitmap)
    surf = pygame.Surface((cols * scale, rows * scale), pygame.SRCALPHA)
    for r, row in enumerate(bitmap):
        for c, ch in enumerate(row):
            if ch == "#":
                if scale == 1:
                    surf.set_at((c, r), (*fg, 255))
                else:
                    pygame.draw.rect(
                        surf, (*fg, 255),
                        (c * scale, r * scale, scale, scale),
                    )
    return surf


